'''
syntax - 
range(start, stop, step) we have to convert it into a list or tuple to see the list of values

start - starting value
stop - value where to stop(stop value is excluded in the output list)
step - steps to print values (2 if we start from 1 it will move 2 step ahead and print 3 and move further similarly)
'''

a = tuple(range(1, 11, 1))
print(a)
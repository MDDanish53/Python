'''
used to check whether a value exists in a sequence(list, tuple, dictionary, string) or not, here we are checking the existence of data value
there are two types of membership operators : 
in - returns true if value exists in sequence
not in - returs true if value does not exists in sequence
'''

vegetables = ["Potato", "Onion", "Cabbage"]

print("Potato" in vegetables)
print("Ladyfinger" not in vegetables)
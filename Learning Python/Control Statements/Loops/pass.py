'''
pass statement is used as a dummy placeholder.
it does not do anything.
but it allows the program to run.
if we do not specify a action to be taken place when a condition is satisfied then we can pass it in order to run the program 
'''

for num in range (1, 10, 1):
    if num == 4:
        pass #not specified the action but the program will run
    print(num)